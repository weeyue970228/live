function getSubAreaAbout(selectObj){
	var areaId=selectObj.value;
	for(var i=4;i<38;i++){
		if (arrarea[i][0]==areaId){return arrarea[i][2]+","+arrarea[i][3];}
	}	
	return "";	
}
function getSubArea(selectObj)
{ 
	var info=getSubAreaAbout(selectObj).split(",");	
	var startIndex=parseInt(info[0]);
	var endIndex=startIndex+parseInt(info[1]);
	var objSubArea=document.getElementById("selectSubArea");	
	objSubArea.options[0]=new Option("请选择...", selectObj.value);
	copyValueToHidden(selectObj.value);
	objSubArea.length=1;
	for(var i=startIndex;i<endIndex;i++){
		objSubArea.options[objSubArea.options.length]=new Option(arrarea[i][1],arrarea[i][0]);
	}	
}

